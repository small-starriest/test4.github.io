    <p><span style="font-size:x-large">T</span>his demo presents <span style="color:green"> AttitudeMiner</span>, a system for mining online discussions. <span style="color:green"> AttitudeMiner</span> uses linguistic techniques to analyze the text exchanged between the participants of a discussion thread at different levels of granularity: the word level, the sentence level, the post level, and the entire thread level. 
    </p>
<p>    The goal of this analysis to identify the polarity of the attitude the discussants carry towards one another. Attitude predictions are then used to construct a signed network representation of the discussion thread. In this network, each discussant is represented by a node. An edge connects two discussants if they exchanged posts. The sign (positive or negative) of the edge is set based on the polarity of the attitude identified in the text associated with the edge. </p>

<p>The system can be used in different applications such as: word polarity identification, identify attitudinal sentences and their signs, signed social network extraction from text, subgroup detect in discussion, and estimate how disputed a discussion topic is.</p>
<p>
In this online interace to this system we demonstrate 4 different uses for the system:
<ol>
<li>Identifying Polarized Words </li>
<li>Identifying Attitudinal Sentences</li>
<li>Extracting Signed Attitude Networks from Text</li>
<li>Detecting Subgroups in online discussions</li>
</ol>
